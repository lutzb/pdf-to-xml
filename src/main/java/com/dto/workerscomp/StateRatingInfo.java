package com.dto.workerscomp;

public class StateRatingInfo {

	private int locationNumber;
	private String classCode;
	private String descriptionCode;
	private String categories;
	private int fullTimeEmployees;
	private int partTimeEmployees;
	private String sic;
	private String naics;
	private String annualRemunerationPayroll;
	private String rate;
	private String annualManualPremium;

	public int getLocationNumber() {
		return locationNumber;
	}

	public void setLocationNumber(int locationNumber) {
		this.locationNumber = locationNumber;
	}

	public String getClassCode() {
		return classCode;
	}

	public void setClassCode(String classCode) {
		this.classCode = classCode;
	}

	public String getDescriptionCode() {
		return descriptionCode;
	}

	public void setDescriptionCode(String descriptionCode) {
		this.descriptionCode = descriptionCode;
	}

	public String getCategories() {
		return categories;
	}

	public void setCategories(String categories) {
		this.categories = categories;
	}

	public int getFullTimeEmployees() {
		return fullTimeEmployees;
	}

	public void setFullTimeEmployees(int fullTimeEmployees) {
		this.fullTimeEmployees = fullTimeEmployees;
	}

	public int getPartTimeEmployees() {
		return partTimeEmployees;
	}

	public void setPartTimeEmployees(int partTimeEmployees) {
		this.partTimeEmployees = partTimeEmployees;
	}

	public String getSic() {
		return sic;
	}

	public void setSic(String sic) {
		this.sic = sic;
	}

	public String getNaics() {
		return naics;
	}

	public void setNaics(String naics) {
		this.naics = naics;
	}

	public String getAnnualRemunerationPayroll() {
		return annualRemunerationPayroll;
	}

	public void setAnnualRemunerationPayroll(String annualRemunerationPayroll) {
		this.annualRemunerationPayroll = annualRemunerationPayroll;
	}

	public String getRate() {
		return rate;
	}

	public void setRate(String rate) {
		this.rate = rate;
	}

	public String getAnnualManualPremium() {
		return annualManualPremium;
	}

	public void setAnnualManualPremium(String annualManualPremium) {
		this.annualManualPremium = annualManualPremium;
	}

}
