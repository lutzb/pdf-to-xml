package com.dto.workerscomp;

public class Producer {

	private String fullName;
	private Address mailingAddress;
	private String producerName;
	private String csrName;
	private String officePhone;
	private String mobilePhone;
	private String faxNumber;
	private String emailAddress;
	private String producerCode;
	private String producerSubCode;
	private String agencyCustomerID;

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public Address getMailingAddress() {
		return mailingAddress;
	}

	public void setMailingAddress(Address mailingAddress) {
		this.mailingAddress = mailingAddress;
	}

	public String getProducerName() {
		return producerName;
	}

	public void setProducerName(String producerName) {
		this.producerName = producerName;
	}
	
	public String getCsrName() {
		return csrName;
	}

	public void setCsrName(String csrName) {
		this.csrName = csrName;
	}

	public String getOfficePhone() {
		return officePhone;
	}

	public void setOfficePhone(String officePhone) {
		this.officePhone = officePhone;
	}

	public String getMobilePhone() {
		return mobilePhone;
	}

	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}

	public String getFaxNumber() {
		return faxNumber;
	}

	public void setFaxNumber(String faxNumber) {
		this.faxNumber = faxNumber;
	}

	public String getEmailAddress() {
		return emailAddress;
	}

	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	public String getProducerCode() {
		return producerCode;
	}

	public void setProducerCode(String producerCode) {
		this.producerCode = producerCode;
	}

	public String getProducerSubCode() {
		return producerSubCode;
	}

	public void setProducerSubCode(String producerSubCode) {
		this.producerSubCode = producerSubCode;
	}

	public String getAgencyCustomerID() {
		return agencyCustomerID;
	}

	public void setAgencyCustomerID(String agencyCustomerID) {
		this.agencyCustomerID = agencyCustomerID;
	}

}
