package com.dto.workerscomp;

public class WorkersCompLocation {
	
	private String highestFloor;
	private WorkersCompAddress address;

	public String getHighestFloor() {
		return highestFloor;
	}

	public void setHighestFloor(String highestFloor) {
		this.highestFloor = highestFloor;
	}

	public WorkersCompAddress getAddress() {
		return address;
	}

	public void setAddress(WorkersCompAddress address) {
		this.address = address;
	}

}
